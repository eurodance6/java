    void updateData() {
        bibliotecas = dbBiblio.getBibliotecas();
        String [][] data = new String[bibliotecas.size()][3];
        for(int i=0;i<bibliotecas.size();i++) {
            data[i][0] = bibliotecas.get(i).getID()+"";
            data[i][1] = bibliotecas.get(i).getNombre();
            data[i][2] = bibliotecas.get(i).getPisos().size()+"";
        }
        tablaDatos.setModel(new NonEditableTableModel(data, TABLE_COLUMNS));
    }
    
    void clean() {
        idLabel.setText("####");
        nameField.setText("");
        setPisos(0);
        pisosSel.clear();
        
        nuevoButton.setEnabled(true);
        btnSeleccionar.setEnabled(false);
        addButton.setEnabled(false);
        updateButton.setEnabled(false);
        btnSeleccionar.setEnabled(true);
        cancelButton.setEnabled(false);
    }
    
    void setPisos(int amount) {
        if(amount==1) lPisos.setText("1 piso");
        else lPisos.setText(amount + " pisos");
    }
    
    void select(Biblioteca b) {
        seleccionada = b;
        idLabel.setText(b.getID() + "");
        setPisos(b.getPisos().size());
        nameField.setText(b.getNombre());
    }
    
