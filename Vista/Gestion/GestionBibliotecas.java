    private void btnEditPisosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditPisosActionPerformed
        // TODO add your handling code here:
        PisoDB pisodb = new PisoDB();
        int[] sel = new int[pisosSel.size()];
        ArrayList<Piso> allPisos = pisodb.getPisos();
        for(int i=0,j=0;i<allPisos.size();i++) {
            if(pisosSel.contains(allPisos.get(i))) {
                sel[j++] = i;
            }
        }
        ListaPisos dialog = new ListaPisos(this, true, (Piso[] pisos) -> {
            pisosSel.clear();
            pisosSel.addAll(Arrays.asList(pisos));
            setPisos(pisosSel.size());
        }, allPisos, sel);
        dialog.setVisible(true);
    }//GEN-LAST:event_btnEditPisosActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        if(seleccionada!=null) {
            seleccionada.setNombre(nameField.getText());
            seleccionada.setPisos(pisosSel);
            dbBiblio.updateBiblioteca(seleccionada);
            select(seleccionada);
            updateData();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

