    //Boton que cambia el estado a Revisado
    private void botonRevisadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRevisadoActionPerformed
        // TODO add your handling code here:
       
        solicitud.setRevisada(true);
        //LLamar Base de Datos
        dbSolicitud.revisar_solicitud(solicitud.getIdSolicitud());
        updateData();
        botonRevisado.setEnabled(false);
    }//GEN-LAST:event_botonRevisadoActionPerformed
    public void cleanTextField(){
        txtCodigoUsuario.setText("");
        txtIDSolicitud.setText("");
        txtFechaSolicitud.setText("");
        txtISBN.setText("");
         txtDescripcionSolicitud.setText("");
    }
    
    public void updateData(){
       
        solicitudes = dbSolicitud.getSolicitudes(0, 1000);
        String [][] data = new String[solicitudes.size()][5];
        for(int i=0;i<solicitudes.size();i++) {
            data[i][0] = solicitudes.get(i).getIdSolicitud()+"";
            data[i][1] = solicitudes.get(i).getFecha_solicitud() + "";
            data[i][2] = solicitudes.get(i).getIsbn()+ "";
            data[i][3] = solicitudes.get(i).getUsuario().getCodigo()+ "";
            data[i][4] = solicitudes.get(i).isRevisada()+ "";
        }
        tablaSolicitudes.setModel(new NonEditableTableModel(data, TABLE_COLUMNS));
    
    }
