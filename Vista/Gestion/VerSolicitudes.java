    public void setSolicitud(Solicitud solicitud){
        txtCodigoUsuario.setText(solicitud.getIdSolicitud() + "");
           txtIDSolicitud.setText(solicitud.getIdSolicitud()+"");
        txtFechaSolicitud.setText(solicitud.getFecha_solicitud()+"");
        txtISBN.setText(solicitud.getIsbn()+ "");
         txtDescripcionSolicitud.setText(solicitud.getDescripcion()+ "");
         botonRevisado.setEnabled(!solicitud.isRevisada());
    }
