    private void btnGenerarPrestamoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarPrestamoActionPerformed
        // TODO add your handling code here:
        ArrayList<Reserva> subset = new ArrayList<>();
        int len = tablaReservas.getRowCount();
        for(int i=0;i<len;i++) {
            Boolean b = (Boolean) tablaReservas.getValueAt(i, 3);
            if(b) subset.add(reservas.get(i));
        }
        
        int tot = PrestamoDB.cuentaPrestamosUsuario(usuario.getCodigo());
        int max = usuario.getMaxReserva();
        
        if((tot + subset.size()) > max) {
            JOptionPane.showMessageDialog(this, "El usuario ya tiene " + tot + " de " + max + "libros prestados",
                    "No se puede prestar", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        GenerarPrestamo prestamo = new GenerarPrestamo(usuario);
        ReservaDB rdb = new ReservaDB();
        rdb.removeAllReservas(subset);
        prestamo.btnGenerarPrestamo.doClick();
        
        for(Reserva r: subset) {
            prestamo.addBook(r.getLibro());
        }
    }//GEN-LAST:event_btnGenerarPrestamoActionPerformed


