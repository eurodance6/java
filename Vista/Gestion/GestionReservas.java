    public void updateReservas() {
        ReservaDB reservadb = new ReservaDB();
        reservas = reservadb.getAllReservas(usuario);
        
        Object [][] data = new Object[reservas.size()][5];
        for(int i=0;i<reservas.size();i++) {
            Reserva reserva = reservas.get(i);
            
            data[i][0] = "" + reserva.getIdReserva();
            data[i][1] = "" + reserva.getLibro().getId();
            data[i][2] = "" + reserva.getLibro().getTitulo();
            data[i][3] = (Boolean)false;
        }
        
        tablaReservas.setModel(new DefaultTableModel(data, COLUMNS){
            @Override
            public Class getColumnClass(int column) {
                switch (column) {
                    case 0:
                        return String.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return Boolean.class;
                }
                return String.class;
            }
            
            @Override
            public boolean isCellEditable(int row, int column) {
                return column==3;
            }
        });
        tablaReservas.getTableHeader().setReorderingAllowed(false);
    }

