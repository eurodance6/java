    public void updateDataUsuarios(){
        String estado = null;
         usuarios = dbUsuario.getUsuarios(0, 100);
        String [][] data = new String[usuarios.size()][6];
        for(int i=0;i<usuarios.size();i++) {
            data[i][0] = usuarios.get(i).getCodigo()+"";
            data[i][1] = usuarios.get(i).getNombre()+ "";
            data[i][2] = usuarios.get(i).getApPaterno() + "";
            data[i][3] = usuarios.get(i).getApMaterno()+ "";
            
            data[i][4] = usuarios.get(i).getTipo().getTipo()+ "";
            if(usuarios.get(i).isActivo()) estado = "Habilitado"; else estado = "Suspendido"; 
            data[i][5] = estado;
        }
        usuariosTable.setModel(new NonEditableTableModel(data, TABLE_COLUMNS));
    }            
