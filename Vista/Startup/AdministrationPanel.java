    @Override
    public void DesktopToolClosing(DesktopTool tool) {
        switch(tool) {
            case BUSCADOR_LIBROS:
                botonBuscadorLibros.setEnabled(true);
                break;
            case REPORTES:
                botonReportes.setEnabled(true);
                break;
            case SOLICITUDES:
                solicitud.setEnabled(true);
                break;
            case GESTION_BIBLIOTECA:
                botonAdminBibliotecas.setEnabled(true);
                break;
        }
    }
