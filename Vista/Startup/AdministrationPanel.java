    public void setNombreBiblioteca(String nombreBiblio) {
        if(nombreBiblio.length()>50) {
            String [] partes = nombreBiblio.split(" ");
            String lineaSup = new String();
            String lineaInf = new String();
            int len1 = nombreBiblio.length()/2;
            
            int i = 0;
            for(int l=0;l<len1 && i<partes.length; i++, l+=partes[i].length()+1)
                lineaSup += partes[i] + " ";
            for(;i<partes.length;i++) lineaInf += partes[i] + " ";
            System.out.println(lineaSup);
            System.out.println(lineaInf);
            labelNombreBiblioteca.setText("<html><body>" + lineaSup + "<br>" + lineaInf + "</body></html>");
        } else labelNombreBiblioteca.setText("<html><body>" + nombreBiblio + "</body></html>");
    }
    
