    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if(tfCodigo.getText().isEmpty() || tfPassword.getPassword().length==0) {
            JOptionPane.showMessageDialog(this, "Debe completar todos los campos para loguearse",
                    "Error en login", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        UsuarioDB userdb = new UsuarioDB();
        int logrado = userdb.login(tfCodigo.getText(), new String(tfPassword.getPassword()));
        if(logrado!=1) {
            JOptionPane.showMessageDialog(this, "Codigo o password incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        dispose();
        
        int cod = Integer.parseInt(tfCodigo.getText());
        
        new AdminPanel(UsuarioDB.getUsuario(cod)).setVisible(true);
    }//GEN-LAST:event_btnAceptarActionPerformed

